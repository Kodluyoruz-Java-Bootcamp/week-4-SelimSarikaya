package com.emlakcepte.client;

public class ProductRequest {
	private Integer amount;
	

	public ProductRequest() {
		super();
	}

	public ProductRequest(Integer amount) {
		super();
		this.amount = amount;
	}

	public Integer getAmount() {
		return amount;
	}

	public void setAmount(Integer amount) {
		this.amount = amount;
	}

}
