package com.emlakcepte.client;

public class Product {
	
	private Integer amount;
	

	public Product() {
		super();
	}

	public Product(Integer amount) {
		super();
		this.amount = amount;
	}

	public Integer getAmount() {
		return amount;
	}

	public void setAmount(Integer amount) {
		this.amount = amount;
	}
	

}
